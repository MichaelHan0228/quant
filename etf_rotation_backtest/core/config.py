#     - 反弹 >= 50%（RECOVERY_THRESHOLD）→ 不止损（反弹足够）
#     - 反弹 < 50% → 止损（反弹不足）
#   执行时机：周五收盘后检查，周一开盘执行
#   作用：应对突发暴跌，但避免在反弹后卖出
